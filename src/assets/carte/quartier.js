// ---------- Helpers ----------
const slug = (s) =>
  String(s || "")
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");

const makeId = (r) =>
  [r.departement, r.commune, r.arrondissement, r.quartier].map(slug).join("__");

const dedupe = (arr) => {
  const map = new Map();
  for (const it of arr) {
    const id = makeId(it);
    if (!map.has(id)) map.set(id, it);
  }
  return [...map.values()];
};

const chunk = (arr, size) => {
  const out = [];
  for (let i = 0; i < arr.length; i += size) out.push(arr.slice(i, i + size));
  return out;
};

