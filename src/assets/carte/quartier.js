/**
 * Remplit la collection "utils/adresses/options" SI ET SEULEMENT SI elle est vide.
 * - √âcrit en multi-batch (<500 writes/batch).
 * - Utilise { merge: true } pour permettre des r√©-ex√©cutions/mises √† jour sans casser.
 *
 * @returns {Promise<{status: "skipped"|"inserted", count: number, batches?: number}>}
 */
export const init_quartiers = async () => {
  const colRef = collection(db, "utils/adresses/options");

  // 1) V√©rifier si la collection est vide (cheap)
  const emptyCheck = await getDocs(fsQuery(colRef, fsLimit(1)));
  if (emptyCheck.size > 0) {
    return { status: "skipped", count: emptyCheck.size };
  }

  // 2) Fusionner + d√©dupliquer
  const rows = dedupe([...littoral, ...atlantique]);
  if (rows.length === 0) return { status: "skipped", count: 0 };

  // 3) Multi-batch (s√©curit√©: 450 par batch pour garder de la marge)
  const MAX_PER_BATCH = 450;
  const chunks = chunk(rows, MAX_PER_BATCH);

  let total = 0;
  for (const part of chunks) {
    const batch = writeBatch(db);
    for (const r of part) {
      const id = makeId(r);
      const docRef = doc(colRef, id);
      batch.set(
        docRef,
        {
          id,
          ...r,
          departement_slug: slug(r.departement),
          commune_slug: slug(r.commune),
          arrondissement_slug: slug(r.arrondissement),
          quartier_slug: slug(r.quartier),
          statut: false,
          tarifs: [],
          createdAt: Date.now(),
        },
        { merge: true }
      );
    }
    await batch.commit();
    total += part.length;
  }

  return { status: "inserted", count: total, batches: chunks.length };
};
